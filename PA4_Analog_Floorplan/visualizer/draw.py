fig, ax = plt.subplots()
max_right = 0
max_top = 0

for line in placement_data.strip().split('\n'):
    parts = re.split(r'\s+', line)
    name = parts[0]
    x = float(parts[1])
    y = float(parts[2])

    # 抓出括號內的數字
    match = re.search(r'\(([^)]+)\)', line)
    width, height = map(float, match.group(1).split()[:2])

    # 更新右邊界與上邊界
    max_right = max(max_right, x + width)
    max_top = max(max_top, y + height)

    # 畫出填滿粉紅色的 rectangle
    rect = plt.Rectangle((x, y), width, height, fill=True, facecolor='pink', edgecolor='blue', linewidth=1)
    ax.add_patch(rect)
    ax.text(x + width/2, y + height/2, name, fontsize=4, ha='center', va='center')

print(f"Right bound: {max_right:.2f}")
print(f"Top bound: {max_top:.2f}")

# 設定圖的範圍與外觀
ax.set_xlim(0, max_right + 10)
ax.set_ylim(0, max_top + 10)
ax.set_aspect('equal')
plt.title("Placement Visualization")
plt.xlabel("X")
plt.ylabel("Y")
plt.grid(True)
plt.show()
